    private void saveMedicineBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveMedicineBtnActionPerformed
        // TODO add your handling code here:
        String medicineName = nameTF.getText();
        String companyName = companyTF.getText();
        String phone = phoneTF.getText();
        String email = emailTF.getText();
        try {
            double costPrice = Double.parseDouble(costTF.getText());
            double salePrice = Double.parseDouble(saleTF.getText());
            int amount = Integer.parseInt(stockTF.getText());
            if(!medicineName.isEmpty() && !companyName.isEmpty() && 
                    !phone.isEmpty() && !email.isEmpty() && phone.matches("^[0-9]*$")){
                DBHandler db = new DBHandler();
                if(db.addNewMedicine(medicineName,companyName,phone,email,costPrice,
                        salePrice,amount)){
                    JOptionPane.showMessageDialog(mainPanel, "Successfully New Medicine Added");                
                    clearData();
                    table.setModel(displayMedicineData());
                    table.getColumnModel().getColumn(0).setPreferredWidth(5);
                    jScrollPane1.setViewportView(table);
                }
                else{
                    JOptionPane.showMessageDialog(mainPanel, "Failed to Add Medicine");
                    clearData();
                }
            }
            else{
                JOptionPane.showMessageDialog(mainPanel, "Please Enter all fields with valid information");
            }            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(mainPanel, "Failed to Add Medicine");
            clearData();
        }        
    }//GEN-LAST:event_saveMedicineBtnActionPerformed

