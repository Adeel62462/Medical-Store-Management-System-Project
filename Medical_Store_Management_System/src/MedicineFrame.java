    public DefaultTableModel displayMedicineData(){
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"ID","Medicine Name","Cost Price",
            "Sale Price","Stock","Company"});
        DBHandler db = new DBHandler();
        ArrayList<Medicine> ar = db.viewAllMedicines();
        int size = ar.size();
        for(int i=0;i<size;i++){
            model.addRow(new Object[]{ar.get(i).getId(),ar.get(i).getName(),
            ar.get(i).getCostPrice(),ar.get(i).getSalePrice(),ar.get(i).getAmount(),
            ar.get(i).getCompanyName()});
        }
        return model;
    }
    
