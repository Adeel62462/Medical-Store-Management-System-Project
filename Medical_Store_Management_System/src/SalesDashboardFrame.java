    private void startBillingBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startBillingBtnActionPerformed
        // TODO add your handling code here:
        String customerName = customerNameTF.getText();
        String mobile = mobileTF.getText();
        if(!customerName.isEmpty() && customerName.matches("^[a-zA-Z' ']*$") &&
                !mobile.isEmpty() && mobile.matches("^[0-9]*$")){
            dashboardPanel.setVisible(true);
            startBillingBtn.setVisible(false);
            customerNameTF.setEditable(false);
            mobileTF.setEditable(false);
        }
        else if(!customerName.matches("^[a-zA-Z' ']*$") || !mobile.matches("^[0-9]*$")){
            JOptionPane.showMessageDialog(mainPanel, "Please Enter Valid Information");
            customerNameTF.setText("");
            mobileTF.setText("");
        }
    }//GEN-LAST:event_startBillingBtnActionPerformed

    private void medicineListBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_medicineListBoxActionPerformed
        // TODO add your handling code here:
        DBHandler db = new DBHandler();
        amount = db.getAmount((String)medicineListBox.getSelectedItem());
        if(amount!=-1){
            availableAmountLbl.setText("Stock:  " + amount);
        }
    }//GEN-LAST:event_medicineListBoxActionPerformed

    private void addItemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addItemBtnActionPerformed
        // TODO add your handling code here:
        try {
            int quantity = Integer.parseInt(quantityTF.getText());
            if(quantity <= amount){
                DBHandler db = new DBHandler();
                String medicineName = medicineListBox.getSelectedItem().toString();
                int id = db.getIdOfMedicine(medicineName);
                double price = db.getPrice(id);
                double total = price * quantity;
                model.addRow(new Object[]{id,medicineName,price,quantity,
                    String.format("%.2f",total)});
                table.setModel(model);
                table.getColumnModel().getColumn(0).setPreferredWidth(5);
                jScrollPane1.setViewportView(table);
                grandTotal = grandTotal + total;
                totalAmountLbl.setText(""+(int)grandTotal);
                db.updateStock(medicineName, quantity*(-1));
            }
            else{
                JOptionPane.showMessageDialog(mainPanel, "Enter Available Quantity");
                quantityTF.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(mainPanel, "Enter a valid Quantity");
            quantityTF.setText("");
        }
    }//GEN-LAST:event_addItemBtnActionPerformed

    
