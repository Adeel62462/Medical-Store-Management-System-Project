    private void saveOrderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveOrderBtnActionPerformed
        // TODO add your handling code here:
        DBHandler db = new DBHandler();
        if(db.saveRecordSale(customerNameTF.getText(),mobileTF.getText(),grandTotal)){
            JOptionPane.showMessageDialog(mainPanel, "Successfully Sale Generated");
            dispose();
        }
        else{
            JOptionPane.showMessageDialog(mainPanel, "Failed To Generate Sale");
        }
    }//GEN-LAST:event_saveOrderBtnActionPerformed

